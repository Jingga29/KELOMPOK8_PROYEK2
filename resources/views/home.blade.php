<script>
    // Ambil data dari PHP dan konversi ke JavaScript
    var labelsRugi = @json($widget['rugi_bulanan']->pluck('tanggal')->toArray());
    var dataRugi = @json($widget['rugi_bulanan']->pluck('jumlah')->toArray());

    // Inisialisasi grafik rugi
    var ctxRugi = document.getElementById('chartRugi').getContext('2d');
    var chartRugi = new Chart(ctxRugi, {
        type: 'bar',
        data: {
            labels: labelsRugi,
            datasets: [{
                label: 'Total Rugi',
                borderColor: 'orange',
                backgroundColor: 'rgba(255, 165, 0, 0.2)',
                data: dataRugi,
            }]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            scales: {
                x: {
                    type: 'category',
                    labels: labelsRugi,
                },
                y: {
                    beginAtZero: true,
                }
            }
        }
    });
</script>
